local function GemMatchesSocket(gemColors, socketColor)
    for _, c in ipairs(gemColors) do
        if c == socketColor then return true end
    end
    return false
end

local function ScoreGemEntry(gem, weights, playerStats, hitCap, expCap, hitType, softCaps)
    -- Single-stat gem (has .stat and .amount)
    if gem.stat then
        return ScoreGemCandidate(gem, weights, playerStats, hitCap, expCap, hitType, softCaps)
    end
    -- Multi-stat gem (has .stats table)
    if gem.stats then
        local total = 0
        for stat, amount in pairs(gem.stats) do
            local pseudo = { stat = stat, amount = amount }
            total = total + ScoreGemCandidate(pseudo, weights, playerStats, hitCap, expCap, hitType, softCaps)
        end
        return total
    end
    return 0
end

local function FindBestGemForSocket(socketColor, weights, playerStats, hitCap, expCap, hitType, softCaps)
    local bestGem, bestScore = nil, 0
    for _, gem in ipairs(gemColorData) do
        if GemMatchesSocket(gem.colors, socketColor) then
            local score = ScoreGemEntry(gem, weights, playerStats, hitCap, expCap, hitType, softCaps)
            if score > bestScore then
                bestScore = score
                bestGem = gem
            end
        end
    end
    return bestGem, bestScore
end

function addon.ResolvePerSocketRecommendation(socketColors, bonusStat, bonusAmount)
    -- socketColors: { "r", "y", "b" } list of empty socket colors
    -- bonusStat: stat key for socket bonus (e.g., "ITEM_MOD_STR_SHORT") or nil
    -- bonusAmount: numeric amount of socket bonus or 0

    if not socketColors or #socketColors == 0 then return nil end

    local currentWeights = addon.GetCurrentWeights()
    if not currentWeights or not next(currentWeights) then return nil end

    local classData = addon.GetClassData()
    local hitCap, expCap = 0, 0
    local hitType = nil
    if classData then
        hitType = classData["HitRatingType"]
        hitCap, expCap = addon.GetCurrentCapSettings()
    end
    local softCaps = addon.GetCurrentSoftCaps and addon.GetCurrentSoftCaps() or {}

    -- Build player stats
    local playerStats = {}
    for stat, _ in pairs(currentWeights) do
        playerStats[stat] = addon.GetCurrentPlayerStatValue(stat) or 0
    end
    if hitCap > 0 and hitType then
        playerStats["ITEM_MOD_HIT_RATING_SHORT"] = playerStats["ITEM_MOD_HIT_RATING_SHORT"] or (GetCombatRating and GetCombatRating(hitType) or 0)
    end
    if expCap > 0 then
        playerStats["ITEM_MOD_EXPERTISE_RATING_SHORT"] = playerStats["ITEM_MOD_EXPERTISE_RATING_SHORT"] or (GetCombatRating and GetCombatRating(CR_EXPERTISE) or 0)
    end
    for stat, _ in pairs(softCaps) do
        if not playerStats[stat] then
            playerStats[stat] = addon.GetCurrentPlayerStatValue(stat) or 0
        end
    end

    -- Strategy A: "Ignore colors" — best single gem × N sockets
    local bisRec = addon.ResolveGemRecommendation()
    local ignoreEP = 0
    if bisRec and bisRec.bis then
        ignoreEP = (bisRec.bis.computedEP or 0) * #socketColors
    end

    -- Strategy B: "Match colors" — best gem per socket + bonus
    local matchGems = {}
    local matchEP = 0
    for _, sc in ipairs(socketColors) do
        local gem, score = FindBestGemForSocket(sc, currentWeights, playerStats, hitCap, expCap, hitType, softCaps)
        table.insert(matchGems, { socketColor = sc, gem = gem, ep = score })
        matchEP = matchEP + score
    end

    -- Add socket bonus EP if all colors matched and bonus is known
    local bonusEP = 0
    if bonusStat and bonusAmount and bonusAmount > 0 then
        local bonusWeight = currentWeights[bonusStat] or 0
        bonusEP = bonusAmount * bonusWeight
        matchEP = matchEP + bonusEP
    end

    -- Compare strategies
    local useColorMatch = matchEP > ignoreEP + 0.01  -- slight threshold to prefer simpler

    return {
        ignoreStrategy = bisRec,
        ignoreEP = ignoreEP,
        matchStrategy = matchGems,
        matchEP = matchEP,
        bonusStat = bonusStat,
        bonusAmount = bonusAmount,
        bonusEP = bonusEP,
        useColorMatch = useColorMatch,
        socketCount = #socketColors,
    }
end

