local function ScoreGemCandidate(candidate, weights, playerStats, hitCap, expCap, hitType, softCaps)
    local stat = candidate.stat
    local amount = candidate.amount
    local weight = weights[stat]
    if not weight or weight <= 0 then return 0 end

    -- Hard cap check for hit
    if stat == "ITEM_MOD_HIT_RATING_SHORT" and hitCap > 0 then
        local curHit = playerStats["ITEM_MOD_HIT_RATING_SHORT"] or 0
        if curHit >= hitCap then return 0 end
        -- Partial: only count the portion under cap
        amount = math.min(amount, hitCap - curHit)
    end

    -- Hard cap check for expertise
    if stat == "ITEM_MOD_EXPERTISE_RATING_SHORT" and expCap > 0 then
        local curExp = playerStats["ITEM_MOD_EXPERTISE_RATING_SHORT"] or 0
        if curExp >= expCap then return 0 end
        amount = math.min(amount, expCap - curExp)
    end

    -- Soft cap check (not for hit/exp which use hard caps above)
    if stat ~= "ITEM_MOD_HIT_RATING_SHORT" and stat ~= "ITEM_MOD_EXPERTISE_RATING_SHORT" then
        local soft = softCaps[stat]
        if soft and type(soft.cap) == "number" and soft.cap > 0 then
            local curVal = playerStats[stat] or 0
            if curVal >= soft.cap then
                -- Entire gem amount is above soft cap
                local mult = type(soft.multiplier) == "number" and soft.multiplier or 1
                if mult < 0 then mult = 0 end
                weight = weight * mult
            elseif curVal + amount > soft.cap then
                -- Partial: split below/above cap portions
                local belowCap = soft.cap - curVal
                local aboveCap = amount - belowCap
                local mult = type(soft.multiplier) == "number" and soft.multiplier or 1
                if mult < 0 then mult = 0 end
                return belowCap * weight + aboveCap * weight * mult
            end
        end
    end

    return amount * weight
end

local function FindBestGemCandidate(pool, weights, playerStats, hitCap, expCap, hitType, softCaps)
    local bestScore = 0
    local bestCandidate = nil
    for _, candidate in ipairs(pool) do
        local score = ScoreGemCandidate(candidate, weights, playerStats, hitCap, expCap, hitType, softCaps)
        if score > bestScore then
            bestScore = score
            bestCandidate = candidate
        end
    end
    return bestCandidate, bestScore
end

function addon.ResolveGemRecommendation()
    local currentWeights = addon.GetCurrentWeights()
    if not currentWeights or not next(currentWeights) then return nil end

    local classData = addon.GetClassData()
    local hitCap, expCap = 0, 0
    local hitType = nil
    if classData then
        hitType = classData["HitRatingType"]
        hitCap, expCap = addon.GetCurrentCapSettings()
    end
    local softCaps = addon.GetCurrentSoftCaps and addon.GetCurrentSoftCaps() or {}

    -- Build player stats snapshot
    local playerStats = {}
    for stat, _ in pairs(currentWeights) do
        playerStats[stat] = addon.GetCurrentPlayerStatValue(stat) or 0
    end
    -- Also gather hit/exp if not in weights
    if hitCap > 0 and hitType then
        playerStats["ITEM_MOD_HIT_RATING_SHORT"] = playerStats["ITEM_MOD_HIT_RATING_SHORT"] or (GetCombatRating and GetCombatRating(hitType) or 0)
    end
    if expCap > 0 then
        playerStats["ITEM_MOD_EXPERTISE_RATING_SHORT"] = playerStats["ITEM_MOD_EXPERTISE_RATING_SHORT"] or (GetCombatRating and GetCombatRating(CR_EXPERTISE) or 0)
    end
    -- Also gather soft-capped stats
    for stat, _ in pairs(softCaps) do
        if not playerStats[stat] then
            playerStats[stat] = addon.GetCurrentPlayerStatValue(stat) or 0
        end
    end

    -- Forward-simulation: find stable BiS
    local bisCandidate, bisScore = FindBestGemCandidate(gemCandidates.bis, currentWeights, playerStats, hitCap, expCap, hitType, softCaps)

    if bisCandidate then
        -- Forward-simulate: add gem stats and recheck
        local MAX_ITER = 3
        for iter = 1, MAX_ITER do
            local simStats = {}
            for k, v in pairs(playerStats) do simStats[k] = v end
            simStats[bisCandidate.stat] = (simStats[bisCandidate.stat] or 0) + bisCandidate.amount

            local newBest, newScore = FindBestGemCandidate(gemCandidates.bis, currentWeights, simStats, hitCap, expCap, hitType, softCaps)
            if not newBest or newBest.stat == bisCandidate.stat then
                break  -- stable
            end
            -- Winner changed. Use self-simulated tiebreak on final iteration.
            if iter == MAX_ITER then
                -- Self-simulation tiebreak: score each candidate with its own stats pre-applied
                local tbBest, tbScore = nil, 0
                for _, candidate in ipairs(gemCandidates.bis) do
                    local selfStats = {}
                    for k, v in pairs(playerStats) do selfStats[k] = v end
                    selfStats[candidate.stat] = (selfStats[candidate.stat] or 0) + candidate.amount
                    local score = ScoreGemCandidate(candidate, currentWeights, selfStats, hitCap, expCap, hitType, softCaps)
                    if score > tbScore then
                        tbScore = score
                        tbBest = candidate
                    end
                end
                if tbBest then
                    bisCandidate = tbBest
                    bisScore = tbScore
                end
            else
                bisCandidate = newBest
                bisScore = newScore
            end
        end
    end

    -- Find budget equivalent (same stat as BiS winner)
    local budgetCandidate, budgetScore = nil, 0
    if bisCandidate then
        for _, candidate in ipairs(gemCandidates.budget) do
            if candidate.stat == bisCandidate.stat then
                budgetScore = ScoreGemCandidate(candidate, currentWeights, playerStats, hitCap, expCap, hitType, softCaps)
                budgetCandidate = candidate
                break
            end
        end
    end

    if not bisCandidate then return nil end

    return {
        bis = {
            name = bisCandidate.name,
            ep_stat = bisCandidate.stat,
            ep_amount = bisCandidate.amount,
            computedEP = bisScore,
        },
        budget = budgetCandidate and {
            name = budgetCandidate.name,
            ep_stat = budgetCandidate.stat,
            ep_amount = budgetCandidate.amount,
            computedEP = budgetScore,
        } or nil,
    }
end

function addon.GetGemRecommendation(className, specName)
    -- Try dynamic cap-aware recommendation first
    local dynamic = addon.ResolveGemRecommendation()
    if dynamic then return dynamic end

    -- Fallback to static archetype recommendation
    local classMap = specArchetypeMap[className]
    if not classMap then return nil end
    local archetype = classMap[specName]
    if not archetype then return nil end
    return gemRecommendations[archetype]
end

