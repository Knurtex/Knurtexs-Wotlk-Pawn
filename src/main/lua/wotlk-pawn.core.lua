function addon.GetMergedWeights(specName)
    local mergedWeights = {}
    local classData = addon.GetClassData()
    local baseWeights = classData and classData[specName]
    if baseWeights then
        for stat, value in pairs(baseWeights) do
            mergedWeights[stat] = value
        end
    end

    local overrides = addon.GetSpecOverrides(specName, false)
    if overrides then
        for stat, value in pairs(overrides) do
            if type(value) == "number" then
                mergedWeights[stat] = value
            end
        end
    end

    return mergedWeights
end

function addon.GetCurrentPlayerStatValue(stat)
    if not stat then
        return 0
    end

    local classData = addon.GetClassData()
    local hitType = classData and classData["HitRatingType"]

    if stat == "ITEM_MOD_HIT_RATING_SHORT" and hitType then
        return GetCombatRating(hitType)
    end

    if stat == "ITEM_MOD_EXPERTISE_RATING_SHORT" then
        return GetCombatRating(CR_EXPERTISE)
    end

    local total = 0
    for slot = 1, 19 do
        local equippedLink = GetInventoryItemLink("player", slot)
        if equippedLink then
            local equippedStats = addon.GetItemStatsWithEnchantAndGems(equippedLink)
            if equippedStats and type(equippedStats[stat]) == "number" then
                total = total + equippedStats[stat]
            end
        end
    end

    return total
end

function addon.CalculateEP(link)
    if not link then return 0 end
    local stats = addon.GetItemStatsWithEnchantAndGems(link)
    if not stats then stats = {} end

    -- Trinket proc/on-use EP fallback: merge pre-computed passive-equivalent stats
    local itemID = tonumber(link:match("item:(%d+)"))
    if itemID and trinketProcData[itemID] then
        for stat, value in pairs(trinketProcData[itemID]) do
            stats[stat] = (stats[stat] or 0) + value
        end
    end

    local classData = addon.GetClassData()
    local currentWeights = addon.GetCurrentWeights()
    local curHit, curExp = 0, 0
    local hitCap, expCap = 0, 0
    local hitType = nil
    local softCaps = addon.GetCurrentSoftCaps and addon.GetCurrentSoftCaps() or {}

    if classData then
        hitType = classData["HitRatingType"]
        hitCap, expCap = addon.GetCurrentCapSettings()
        if hitType then curHit = GetCombatRating(hitType) end
        if expCap > 0 then curExp = GetCombatRating(CR_EXPERTISE) end
    end

    local totalEP = 0
    local hitCapped = hitCap > 0 and curHit >= hitCap
    local expCapped = expCap > 0 and curExp >= expCap

    for stat, value in pairs(stats) do
        local weight = currentWeights[stat]
        if weight then
            local apply = true
            if stat == "ITEM_MOD_HIT_RATING_SHORT" then
                if hitCapped then
                    apply = false
                end
            end
            if stat == "ITEM_MOD_EXPERTISE_RATING_SHORT" then
                if expCapped then
                    apply = false
                end
            end
            if apply then
                local soft = softCaps[stat]
                if stat == "ITEM_MOD_HIT_RATING_SHORT" or stat == "ITEM_MOD_EXPERTISE_RATING_SHORT" then
                    soft = nil
                end

                if soft and type(soft.cap) == "number" and soft.cap > 0 then
                    local currentStatValue = addon.GetCurrentPlayerStatValue(stat)
                    if currentStatValue >= soft.cap then
                        local softMultiplier = type(soft.multiplier) == "number" and soft.multiplier or 1
                        if softMultiplier < 0 then
                            softMultiplier = 0
                        end
                        weight = weight * softMultiplier
                    end
                end
            end
            if apply then totalEP = totalEP + (value * weight) end
        end
    end

    return totalEP
end

function addon.UpdateActiveWeights()
    local currentLevel = UnitLevel("player")
    local activeWeights = GetActiveWeightsTable()
    if addon.GetAllWeights() ~= activeWeights then
        addon.SetAllWeights(activeWeights)
    end
    addon.SetClassData(addon.GetAllWeights()[class] or { Specs = {""} })
    addon.SetLastDetectedLevel(currentLevel)

    local classData = addon.GetClassData()
    local forcedSpec = addon.GetForcedSpecOverride and addon.GetForcedSpecOverride() or nil

    local function HasSpecName(specName)
        if not specName or specName == "" then
            return false
        end
        for _, existing in ipairs(classData["Specs"] or {}) do
            if existing == specName then
                return true
            end
        end
        return false
    end

    local detectedSpec = nil
    if not forcedSpec then
        local activeGroup = GetActiveTalentGroup and GetActiveTalentGroup() or 1
        local maxPoints, specIndex = -1, 0
        for i = 1, #(classData["Specs"] or {}) do
            local _, _, pointsSpent = GetTalentTabInfo(i, nil, nil, activeGroup)
            if pointsSpent and pointsSpent > maxPoints then
                maxPoints = pointsSpent
                specIndex = i
            end
        end

        if specIndex > 0 and classData["Specs"] and classData["Specs"][specIndex] then
            detectedSpec = classData["Specs"][specIndex]
        end
        addon.SetLastDetectedSpec(detectedSpec)
    end

    local effectiveSpec = nil
    if forcedSpec and HasSpecName(forcedSpec) then
        effectiveSpec = forcedSpec
    else
        effectiveSpec = detectedSpec
    end

    if effectiveSpec then
        addon.SetCurrentSpecName(effectiveSpec)
        addon.SetCurrentWeights(addon.GetMergedWeights(effectiveSpec))
    else
        addon.SetCurrentSpecName("")
        addon.SetCurrentWeights({})
    end
end
