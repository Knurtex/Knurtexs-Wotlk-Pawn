local reportedUnknownEnchants = {}

function addon.GetEnchantStats(link)
    if not link then return nil end
    local enchantID = tonumber(link:match("item:%d+:(%d+)"))
    if not enchantID or enchantID == 0 then return nil end
    local data = enchantData[enchantID]
    if not data and not reportedUnknownEnchants[enchantID] then
        reportedUnknownEnchants[enchantID] = true
    end
    return data
end

function addon.GetGemStats(gemLink)
    if not gemLink then return nil end
    local gemID = tonumber(gemLink:match("item:(%d+)"))
    if not gemID then return nil end
    return gemData[gemID]
end

function addon.GetItemStatsWithEnchantAndGems(link)
    if not link then return nil end
    local stats = GetItemStats(link)
    if not stats then return nil end

    local merged = {}
    for k, v in pairs(stats) do
        merged[k] = v
    end

    -- Merge enchant stats
    local enchantStats = addon.GetEnchantStats(link)
    if enchantStats then
        for k, v in pairs(enchantStats) do
            merged[k] = (merged[k] or 0) + v
        end
    end

    -- Merge gem stats
    if GetItemGem then
        for i = 1, 3 do
            local gemName, gemLink = GetItemGem(link, i)
            if gemLink then
                local gemStats = GetItemStats(gemLink)
                if not gemStats or not next(gemStats) then
                    gemStats = addon.GetGemStats(gemLink)
                end
                if gemStats then
                    for k, v in pairs(gemStats) do
                        merged[k] = (merged[k] or 0) + v
                    end
                end
            end
        end
    end

    return merged
end

