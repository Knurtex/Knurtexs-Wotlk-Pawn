local function ScoreEnchantCandidate(enchant, weights, playerStats, hitCap, expCap, hitType, softCaps)
    local totalEP = 0
    for stat, amount in pairs(enchant.stats) do
        local pseudo = { stat = stat, amount = amount }
        totalEP = totalEP + ScoreGemCandidate(pseudo, weights, playerStats, hitCap, expCap, hitType, softCaps)
    end
    return totalEP
end

function addon.ResolveEnchantRecommendation(itemEquipLoc, itemLink)
    local slotCategory = equipLocToEnchantSlot[itemEquipLoc]
    if not slotCategory then return nil end

    local candidates = slotEnchantCandidates[slotCategory]
    if not candidates or #candidates == 0 then return nil end

    local currentWeights = addon.GetCurrentWeights()
    if not currentWeights or not next(currentWeights) then return nil end

    local classData = addon.GetClassData()
    local hitCap, expCap = 0, 0
    local hitType = nil
    if classData then
        hitType = classData["HitRatingType"]
        hitCap, expCap = addon.GetCurrentCapSettings()
    end
    local softCaps = addon.GetCurrentSoftCaps and addon.GetCurrentSoftCaps() or {}

    -- Build player stats
    local playerStats = {}
    for stat, _ in pairs(currentWeights) do
        playerStats[stat] = addon.GetCurrentPlayerStatValue(stat) or 0
    end
    if hitCap > 0 and hitType then
        playerStats["ITEM_MOD_HIT_RATING_SHORT"] = playerStats["ITEM_MOD_HIT_RATING_SHORT"] or (GetCombatRating and GetCombatRating(hitType) or 0)
    end
    if expCap > 0 then
        playerStats["ITEM_MOD_EXPERTISE_RATING_SHORT"] = playerStats["ITEM_MOD_EXPERTISE_RATING_SHORT"] or (GetCombatRating and GetCombatRating(CR_EXPERTISE) or 0)
    end
    for stat, _ in pairs(softCaps) do
        if not playerStats[stat] then
            playerStats[stat] = addon.GetCurrentPlayerStatValue(stat) or 0
        end
    end

    -- Score all candidates
    local bestEnchant, bestScore = nil, 0
    for _, enchant in ipairs(candidates) do
        local score = ScoreEnchantCandidate(enchant, currentWeights, playerStats, hitCap, expCap, hitType, softCaps)
        if score > bestScore then
            bestScore = score
            bestEnchant = enchant
        end
    end

    if not bestEnchant then return nil end

    -- Score current enchant on the item (if any)
    local currentEnchantEP = 0
    local currentEnchantID = nil
    local isCurrentBiS = false
    if itemLink then
        currentEnchantID = tonumber(itemLink:match("item:%d+:(%d+)"))
        if currentEnchantID and currentEnchantID > 0 then
            local currentStats = enchantData[currentEnchantID]
            if currentStats then
                local tempEnchant = { stats = currentStats }
                currentEnchantEP = ScoreEnchantCandidate(tempEnchant, currentWeights, playerStats, hitCap, expCap, hitType, softCaps)
            end
            isCurrentBiS = (currentEnchantID == bestEnchant.enchantID)
        end
    end

    return {
        best = bestEnchant,
        bestEP = bestScore,
        currentEnchantID = currentEnchantID,
        currentEnchantEP = currentEnchantEP,
        isCurrentBiS = isCurrentBiS,
        hasEnchant = (currentEnchantID ~= nil and currentEnchantID > 0),
    }
end

